def default_config(pdf_type: str) -> dict:
    """Return baseline config for a PDF type (semua section enabled, branding kosong)."""
    sections = [s["key"] for s in PDF_SECTION_DEFINITIONS.get(pdf_type, []) if s.get("default_on", True)]
    return {
        "pdf_type": pdf_type,
        "sections": sections,
        "header": {
            "line1": "",      # default akan ambil dari company_settings
            "line2": "",
            "show_logo": True,
        },
        "footer": {
            "text": "",
            "show_page_number": True,
            "show_print_meta": True,
        },
        "branding": {
            "logo_object_path": "",
            "watermark_text": "",
            "watermark_opacity": 0.08,
            "primary_color": "",   # hex; default akan pakai warna template
            "accent_color": "",
        },
        "page": {
            "size": "A4" if pdf_type in ("lkp", "shift-report") else "A5",
            "orientation": "portrait",
            "margins": {"top": 18, "bottom": 16, "left": 14, "right": 14},   # mm
        },
        "typography": {
            "font_family": "Helvetica",
            "font_scale": 1.0,
        },
        "format": {
            "currency": "IDR",
            "currency_symbol": "Rp",
            "date_format": "DD/MM/YYYY",
            "number_grouping": ".",
            "decimal_separator": ",",
        },
        "language": "id",
        "custom_labels": {},
    }


def merge_config(stored: Optional[dict], pdf_type: str) -> dict:
    """Merge a stored preset (mungkin parsial / legacy kolom-only) dengan default."""
    base = default_config(pdf_type)
    if not stored:
        return base
    # Top-level deep merge for known nested dicts
    for k in ("header", "footer", "branding", "page", "typography", "format"):
        if isinstance(stored.get(k), dict):
            merged = {**base[k], **stored[k]}
            # nested margins
            if k == "page" and isinstance(stored[k].get("margins"), dict):
                merged["margins"] = {**base["page"]["margins"], **stored[k]["margins"]}
            base[k] = merged
    if isinstance(stored.get("sections"), list):
        base["sections"] = stored["sections"]
    if isinstance(stored.get("language"), str):
        base["language"] = stored["language"]
    if isinstance(stored.get("custom_labels"), dict):
        base["custom_labels"] = stored["custom_labels"]
    if isinstance(stored.get("columns"), list):
        base["columns"] = stored["columns"]
    if "name" in stored: base["name"] = stored["name"]
    if "id" in stored: base["id"] = stored["id"]
    if "is_default" in stored: base["is_default"] = stored["is_default"]
    return base


async def resolve_pdf_config(db, pdf_type: str, config_id: Optional[str] = None) -> dict:
    """Load + merge config preset; falls back to type default if none."""
    cfg = None
    if config_id:
        cfg = await db.pdf_export_configs.find_one({"id": config_id})
    if not cfg:
        cfg = await db.pdf_export_configs.find_one({"pdf_type": pdf_type, "is_default": True})
    return merge_config(cfg, pdf_type)


