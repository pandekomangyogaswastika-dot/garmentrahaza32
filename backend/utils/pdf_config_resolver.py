# Format helpers
# ─────────────────────────────────────────────────────────────────
def _to_int(v: Any, default: int = 0) -> int:
    try:
        return int(round(float(v or 0)))
    except (TypeError, ValueError):
        return default


def format_number(config: dict, value: Any, decimals: int = 0) -> str:
    """Group thousand separator + decimal separator per config."""
    fmt = (config or {}).get("format", {}) or {}
    grp = fmt.get("number_grouping", ".")
    dec = fmt.get("decimal_separator", ",")
    try:
        n = float(value or 0)
    except (TypeError, ValueError):
        n = 0.0
    if decimals == 0:
        s = f"{int(round(n)):,}"
        return s.replace(",", grp)
    s = f"{n:,.{decimals}f}"
    # default uses , as thousand and . as decimal — swap to user pref
    s = s.replace(",", "\x00").replace(".", dec).replace("\x00", grp)
    return s


def format_currency(config: dict, value: Any) -> str:
    sym = ((config or {}).get("format", {}) or {}).get("currency_symbol", "Rp")
    return f"{sym} {format_number(config, value, 0)}"


def format_date(config: dict, value: Any) -> str:
    """Accepts date / datetime / str (YYYY-MM-DD or ISO). Returns formatted string."""
    if value is None or value == "":
        return "-"
    if isinstance(value, datetime):
        d = value
    elif isinstance(value, date):
        d = datetime(value.year, value.month, value.day, tzinfo=timezone.utc)
    else:
        s = str(value)[:19]
        # handle YYYY-MM-DD
        try:
            d = datetime.fromisoformat(s.replace("Z", "+00:00"))
        except ValueError:
            try:
                d = datetime.strptime(s[:10], "%Y-%m-%d")
            except ValueError:
                return str(value)
    fmt = ((config or {}).get("format", {}) or {}).get("date_format", "DD/MM/YYYY")
    mapping = {
        "DD/MM/YYYY":   "%d/%m/%Y",
        "DD-MM-YYYY":   "%d-%m-%Y",
        "YYYY-MM-DD":   "%Y-%m-%d",
        "MM/DD/YYYY":   "%m/%d/%Y",
        "DD MMM YYYY":  "%d %b %Y",
        "DD MMMM YYYY": "%d %B %Y",
    }
    py_fmt = mapping.get(fmt, "%d/%m/%Y")
    try:
        return d.strftime(py_fmt)
    except (ValueError, TypeError):
        return str(value)


# ─────────────────────────────────────────────────────────────────
# Page size helper
# ─────────────────────────────────────────────────────────────────
def get_page_size(config: dict):
    """Return reportlab pagesize tuple based on config.page.size + orientation."""
    from reportlab.lib.pagesizes import A4, A5, LETTER, landscape, portrait
    page = (config or {}).get("page", {}) or {}
    size = page.get("size", "A4").upper()
    orient = page.get("orientation", "portrait").lower()
    base = {"A4": A4, "A5": A5, "LETTER": LETTER}.get(size, A4)
    return landscape(base) if orient == "landscape" else portrait(base)


def get_margins_mm(config: dict) -> tuple[float, float, float, float]:
    """Return (top, bottom, left, right) margins in mm."""
    m = ((config or {}).get("page", {}) or {}).get("margins", {}) or {}
    return (
        float(m.get("top", 18)),
        float(m.get("bottom", 16)),
        float(m.get("left", 14)),
        float(m.get("right", 14)),
    )


# ─────────────────────────────────────────────────────────────────
# Type metadata for UI consumption
# ─────────────────────────────────────────────────────────────────
def get_type_metadata(pdf_type: str, lang: str = "id") -> dict:
    """Return metadata for a PDF type (sections + customizable label keys + default labels)."""
    sections = []
    for s in PDF_SECTION_DEFINITIONS.get(pdf_type, []):
        lk = s.get("label_key", "")
        labels = DEFAULT_LABELS.get(lk, {})
        sections.append({
            "key": s["key"],
            "label_id": labels.get("id", s["key"]),
            "label_en": labels.get("en", s["key"]),
            "default_on": s.get("default_on", True),
            "required": s.get("required", False),
        })
    customizable = []
    for k in PDF_CUSTOMIZABLE_LABELS.get(pdf_type, []):
        labels = DEFAULT_LABELS.get(k, {})
        customizable.append({
            "key": k,
            "default_id": labels.get("id", k),
            "default_en": labels.get("en", k),
        })
    return {
        "pdf_type": pdf_type,
        "sections": sections,
        "customizable_labels": customizable,
        "default_config": default_config(pdf_type),
    }
