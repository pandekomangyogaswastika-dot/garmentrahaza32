def is_section_on(config: dict, section_key: str) -> bool:
    enabled = config.get("sections")
    if enabled is None:
        return True   # default = on if not configured
    return section_key in enabled


# ─────────────────────────────────────────────────────────────────
# Label translator
# ─────────────────────────────────────────────────────────────────
def get_label(config: dict, key: str, fallback: Optional[str] = None) -> str:
    """Resolve label dari custom_labels → DEFAULT_LABELS → fallback string."""
    lang = (config or {}).get("language", "id")
    custom = (config or {}).get("custom_labels", {}) or {}
    if key in custom:
        v = custom[key]
        if isinstance(v, dict):
            return v.get(lang) or v.get("id") or v.get("en") or (fallback or key)
        if isinstance(v, str) and v.strip():
            return v
    if key in DEFAULT_LABELS:
        d = DEFAULT_LABELS[key]
        return d.get(lang) or d.get("id") or d.get("en") or (fallback or key)
    return fallback if fallback is not None else key


